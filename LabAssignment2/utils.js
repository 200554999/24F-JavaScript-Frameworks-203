// Print a welcome message
export function printWelcomeMessage(Name1, Name2, Name3) {
    console.log(`Welcome ${Name1}, ${Name2} & ${Name3} to Georgian Ilac College`);
}

// Convert Celsius to Fahrenheit
export function convertCelsiusToFahrenheit(celsius) {
    return (celsius * 9/5) + 32;
}

// Function to multiply three numbers
export function multiplyThreeNumbers(a, b, c) {
    return a * b * c;
}

// An advanced function (just returning a random number)
export function advancedFunction() {
    return Math.random();
}
